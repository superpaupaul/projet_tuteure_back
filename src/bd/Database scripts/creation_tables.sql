    admin_id INT PRIMARY KEY AUTO_INCREMENT, 
    nom VARCHAR(25) NOT NULL,
    prenom VARCHAR(25),
    email VARCHAR(50) CHECK(email LIKE '%@%.%')
) AUTO_INCREMENT=1;


